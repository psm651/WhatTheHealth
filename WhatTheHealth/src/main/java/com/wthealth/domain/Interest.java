package com.wthealth.domain;

public class Interest {
	
	private String postNo;
	private int interstNo;
	private String userId;
	private String interestStatus;
	
	public Interest() {
	}

	public String getPostNo() {
		return postNo;
	}

	public void setPostNo(String postNo) {
		this.postNo = postNo;
	}

	public int getInterstNo() {
		return interstNo;
	}

	public void setInterstNo(int interstNo) {
		this.interstNo = interstNo;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getInterestStatus() {
		return interestStatus;
	}

	public void setInterestStatus(String interestStatus) {
		this.interestStatus = interestStatus;
	}
	
	
}
